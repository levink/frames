#include "mesh.h"

using std::vector;
using glm::vec2;

ImageMesh ImageMesh::createImageMesh(int w, int h) {
    auto position = vector<vec2> {
        { 0, 0 },
        { w, 0 },
        { w, h },
        { 0, h }
    };
    auto texture = vector<vec2> {
        { 0, 1 },
        { 1, 1 },
        { 1, 0 },
        { 0, 0 }
    };
    auto face = vector<GLFace>{
       { 0, 1, 2 },
       { 2, 3, 0 }
    };
    return ImageMesh{
        std::move(position),
        std::move(texture),
        std::move(face)
    };
}


